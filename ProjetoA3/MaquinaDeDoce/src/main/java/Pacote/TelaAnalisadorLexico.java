    public static int countOccurrences(String linha, String lexema) {
    int count = 0;
    int lastIndex = 0;
    while ((lastIndex = linha.indexOf(lexema, lastIndex)) != -1) {
        count++;
        lastIndex += lexema.length();
    }
    return count;
}
